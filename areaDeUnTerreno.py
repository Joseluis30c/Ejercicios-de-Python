### Hacer un programa que permita hallar el area de un terreno
print("Calculo del area de un terreno")
l=int(input("Ingresa el largo del terreno "))
a=int(input("Ingresa el ancho del terreno "))
area=l*a
print("El area del terreno de",l," mts de largo y ",a," mts de ancho, es igual a: " )
print(area, " mets cuadrados")
